const NOINDEX_PREFIXES = [
  '/admin',
  '/onboarding',
  '/phlebotomist', '/doctor', '/nurse', '/caregiver', '/physio',
  '/radiology', '/pharmacy', '/emergency', '/dispatch', '/corporate',
  '/training', '/it-support', '/call-center', '/sales', '/finance',
  '/analytics', '/qa', '/inventory', '/telemedicine', '/legal', '/content',
  '/emergency-coordinator',
  '/checkout',
];

function isNoindex(pathname) {
  return NOINDEX_PREFIXES.some(p => pathname === p || pathname.startsWith(p + '/'));
}

function isStaticAsset(pathname) {
  // PWA: /manifest.webmanifest MUST be served as a real file (not SPA HTML).
  // Without this, Chrome never fires beforeinstallprompt and Install does nothing.
  return /\.(js|css|png|svg|ico|json|webmanifest|woff2?|ttf|eot|webp|jpg|jpeg|gif|pdf|map|txt|xml)$/i.test(pathname)
    || pathname.startsWith('/assets/')
    || pathname.startsWith('/favicon')
    || pathname.startsWith('/logo.')
    || pathname.startsWith('/icons/')
    || pathname === '/sw.js'
    || pathname === '/manifest.webmanifest'
    || pathname === '/manifest.json';
}

function escapeHtml(str) {
  return str.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/'/g, '&#39;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function fallbackMeta(pathname) {
  const segments = pathname.split('/').filter(Boolean);
  const last = segments[segments.length - 1] || 'Home';
  const display = last.replace(/[-/]/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
  return {
    title: `${display} — Jeevan HealthCare at Home`,
    description: `Learn about ${display} at Jeevan HealthCare at Home. Book trusted healthcare services at home with free doorstep collection.`,
    ogTitle: `${display} | Jeevan HealthCare`,
    ogDescription: `Learn about ${display} at Jeevan HealthCare. Book trusted healthcare at home.`,
    ogImage: '/logo.png',
  };
}

function matchDynamicRoute(pathname) {
  for (const pattern of DYNAMIC_ROUTE_PATTERNS) {
    const match = pathname.match(pattern.test);
    if (match) {
      return pattern.build(match[1]);
    }
  }
  return null;
}

function getMeta(pathname) {
  return ROUTE_META[pathname] || matchDynamicRoute(pathname) || fallbackMeta(pathname);
}

function injectMeta(html, meta, pathname) {
  const noindex = isNoindex(pathname);
  const fullUrl = `https://jeevanhealthcare.com${pathname}`;

  let out = html;

  out = out.replace(/<title>.*?<\/title>/, `<title>${escapeHtml(meta.title)}</title>`);

  out = out.replace(
    /<meta\s+name="description"[^>]*\/?>/i,
    `<meta name="description" content="${escapeHtml(meta.description)}" />`
  );

  const tags = [];
  tags.push(`<meta property="og:title" content="${escapeHtml(meta.ogTitle || meta.title)}" />`);
  tags.push(`<meta property="og:description" content="${escapeHtml(meta.ogDescription || meta.description)}" />`);
  tags.push(`<meta property="og:image" content="${escapeHtml(meta.ogImage || '/logo.png')}" />`);
  tags.push(`<meta property="og:url" content="${escapeHtml(fullUrl)}" />`);
  tags.push(`<meta property="og:type" content="website" />`);
  tags.push(`<meta name="twitter:card" content="summary_large_image" />`);
  tags.push(`<meta name="twitter:title" content="${escapeHtml(meta.ogTitle || meta.title)}" />`);
  tags.push(`<meta name="twitter:description" content="${escapeHtml(meta.ogDescription || meta.description)}" />`);
  tags.push(`<meta name="twitter:image" content="${escapeHtml(meta.ogImage || '/logo.png')}" />`);
  tags.push(`<link rel="canonical" href="${escapeHtml(fullUrl)}" />`);

  if (noindex) {
    tags.push(`<meta name="robots" content="noindex,nofollow" />`);
  }

  const jsonld = {
    '@context': 'https://schema.org',
    '@type': 'MedicalWebPage',
    name: meta.title,
    description: meta.description,
    url: fullUrl,
    publisher: {
      '@type': 'Organization',
      name: 'Jeevan HealthCare at Home',
      url: 'https://jeevanhealthcare.com',
      logo: 'https://jeevanhealthcare.com/logo.png',
    },
    inLanguage: 'en',
  };

  tags.push(`<script type="application/ld+json">${JSON.stringify(jsonld)}</script>`);

  out = out.replace('</head>', `${tags.join('\n    ')}\n  </head>`);

  return out;
}

export default {
  async fetch(request, env) {
    const url = new URL(request.url);
    const pathname = url.pathname;

    try {
      if (isStaticAsset(pathname) || pathname === '/robots.txt' || pathname === '/sitemap.xml') {
        const assetResp = await env.ASSETS.fetch(request);
        const assetType = assetResp.headers.get('content-type') || '';
        // Cloudflare SPA fallback can serve index.html (text/html) for a missing
        // asset with a 200 status. Returning HTML for a .js/.css request causes a
        // hard MIME-type failure ("Expected a JavaScript-or-Wasm module script but
        // the server responded with a MIME type of text/html").
        // Treat both a 404 and any HTML response as "not found" for assets.
        if (assetResp.status === 404 || assetType.includes('text/html')) {
          // Do NOT cache 404/error responses at the CDN edge — the immutable
          // Cache-Control from _headers would otherwise poison the cache for 1y.
          return new Response('Not Found', {
            status: 404,
            headers: {
              'content-type': 'text/plain',
              'cache-control': 'no-cache, no-store, must-revalidate',
            },
          });
        }
        return assetResp;
      }

      const indexResp = await env.ASSETS.fetch(new URL('/index.html', url));
      if (!indexResp.ok) {
        return env.ASSETS.fetch(request);
      }

      const html = await indexResp.text();
      const meta = getMeta(pathname);
      const modified = injectMeta(html, meta, pathname);

      return new Response(modified, {
        headers: {
          'content-type': 'text/html;charset=UTF-8',
          // Never cache index.html at the edge — a stale HTML page references
          // old hashed chunk names that no longer exist, causing MIME failures.
          // Browsers must always revalidate so new deploys take effect.
          'cache-control': 'public, max-age=0, must-revalidate',
        },
      });
    } catch (err) {
      try {
        return env.ASSETS.fetch(request);
      } catch {
        return new Response('Not Found', { status: 404 });
      }
    }
  },
};
